public class RainbowApp {
	void rainbowCheck(String rainbow) {
		if(rainbow.equalsIgnoreCase("violet")) {
			System.out.println("The given color "+rainbow+" is in rainbow");
		}
		else if(rainbow.equalsIgnoreCase("indigo")) {
			System.out.println("The given color "+rainbow+" is in rainbow");
		}
		else if(rainbow.equalsIgnoreCase("blue")) {
			System.out.println("The given color "+rainbow+" is in rainbow");
		}
		else if(rainbow.equalsIgnoreCase("green")) {
			System.out.println("The given color "+rainbow+" is in rainbow");
		}
		else if(rainbow.equalsIgnoreCase("yellow")) {
			System.out.println("The given color "+rainbow+" is in rainbow");
		}
		else if(rainbow.equalsIgnoreCase("orange")) {
			System.out.println("The given color "+rainbow+" is in rainbow");
		}
		else if(rainbow.equalsIgnoreCase("red")) {
			System.out.println("The given color "+rainbow+" is in rainbow");
		}
		else {
			System.out.println("The given color "+rainbow+" is not in rainbow");
		}
	}

}
